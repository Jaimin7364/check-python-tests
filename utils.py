def sum_three_numbers(a, b, c):
    """
    Calculates the sum of three numbers.

    Args:
        a (int or float): The first number.
        b (int or float): The second number.
        c (int or float): The third number.

    Returns:
        int or float: The result of a + b + c.
    """
    return a + b + c
